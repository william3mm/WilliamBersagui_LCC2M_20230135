    private void btn_entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_entrarActionPerformed
        // TODO add your handling code here:
        
        String nome = btn_nome.getText();
        
        String password = btn_senha.getText();
        
        String nome_admin = "William";
        
        String password_admin = "1234";
        
        AdminWilliam admin = new AdminWilliam();
        
        admin.setNome(nome);
        
        admin.setPassword(password);
        
        
        
        if(nome.equals(nome_admin) && password.equals(password_admin) ){
            
            TelaInicial tela = new TelaInicial();
            
            tela.setVisible(true);
        
        }else if(nome.isEmpty() || password.isEmpty()){
            
            JOptionPane.showMessageDialog(rootPane, "OS CAMPOS N√ÉO PODEM ESTAR VAZIOS");
            
            
            
        } else{
            
            JOptionPane.showMessageDialog(rootPane, "NOME DO ADMIN OU PASSWORD ERRADA");
        }
    }//GEN-LAST:event_btn_entrarActionPerformed

